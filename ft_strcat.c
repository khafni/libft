#include "libft.h"

char	*ft_strcat(char *s1, char *s2)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s1[i] != '\0')
	{
		i++;
	}
	while (s2[i] != '\0')
	{
		s1[i] = s2[j];
		i++;
		j++;
	}
	s2[i] = '\0';
	return (s1);
}
